# integer types
a = 13
b = 100
c = -66
print(a, b, c)
print(type(a))

# floating point types
x = 33.5
y = -25.8
z = 205
print(x, y, z)
print(type(z))

# complex type
d = 3 + 5j
print(d)
print(type(d))

# binary
e = 0b1010
print(e)
print(type(e))

# hexadecimal
f = 0xFF
print(f)
print(type(f))

# octal
o = 0o12
print(o)
print(type(o))

# boolean
g = True
print(g)
print(type(g))
print(9 < 8)

# convert float to int
h = int(x)
print(h)
print(type(h))

# convert int to float
i = float("22.5")
print(i)
print(type(i))

# convert to binary, hexadecimal, octal
print(bin(10))
print(hex(10))
print(oct(10))
